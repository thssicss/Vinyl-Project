  modal.style.display = "block";

  const container = document.getElementById("popup-container");

  const popupElement = document.createElement("article");
  popupElement.className = `section__popup`;

  const titleElement = document.createElement("h1");
  titleElement.className = `section__h1 section__h1--popup`;
  titleElement.textContent = data.artist;

  const dateElement = document.createElement("p");
  dateElement.className = `section__date`;
  dateElement.innerHTML = '<b>Data de Nascimiento:</b> ' + data.dateOfBirth;

  const placeElement = document.createElement("p");
  placeElement.className = `section__place`;
